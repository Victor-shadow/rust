#[allow(unused_variables)]
fn main() {
    //addition
    let sum = 100 + 100;
    println!("The sum is: {}", sum);
    //subtraction
    let difference = 1000.0 - 100.0;
    println!("The difference is: {}", difference);
    //multiplication
    let product =  1000.0 * 2000.0;
    println!("The product is: {}",product);
    //division
    let quotient = 1000.0 / 10.0;
    println!("The quotient is: {}", quotient);
    let truncated = -5 / 3; //results in -1
    println!("The truncated operation is: {}", truncated);
    //remainder
    let remainder = 43 % 5;
    println!("The remainder is: {}", remainder);
}